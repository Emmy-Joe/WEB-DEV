            <p class="mb-4"> </p>

              
            <ol>
              <li id="p1"> <b>THE ANCIENT LANDMARK</b>
                <p>
                The Bible doctrine on restitution spans virtually all the dispensations of God‟s dealing with men. The revelation of God‟s will and Word before the Mosaic laws demands that we make restitutions (Romans 1:19,20). The doctrine is thus not abolished with the law. Also, restitution is part of the moral law. Most sins committed by men are against God as well as against man. Therefore, repentance toward God for the children of Israel, included restoration of all properties gained by fraud, lying or pretence (Exodus 21:18,19,22,26-36; 22:1-15; Leviticus 6:2-7; Num. 5:6-8). Rulers in Israel also reflected and embraced this teaching on restitution (II Samuel 16:5-8 cf. 19:16-23; Proverbs 6:30,31; I Kings 20:34; II Kings 8:1-6). The prophets in Israel as well preached, explained and emphasised the need to make restitution by every backslidden Israelite who was returning to God (Ezekiel 33:14-16).
                </p>
                </li>
              
                  <br>
                
                  <li id="p2"> <b>THE MESSIANIC EMPHASIS</b>
                    <p>
                    Jesus emphasised the doctrine of restitution. “Till heaven and earth pass, one jot or one tittle shall in no wise pass from the law till all be fulfilled.” (Matthew 18:15-17; Luke 17:3-5; 19:8-10). In the Words of Jesus, restitution even comes before whatever sacrifice we may bring before the Lord (Matthew 5:23,24). The encounter of Zacchæus with the Lord is a clear example. Zacchæus wanted nothing to stand between himself and the salvation experience - not even his wealth, especially the part that was acquired in a wrong way. Though the people murmured that Jesus was gone to be a sinner‟s guest, Jesus pronounced that salvation had come to Zacchæus‟ house, being a son of Abraham, now especially by faith. Zacchæus was prepared to settle his past life, not only with God, but with men (Luke 19:1-10). The salvation experience constraints a person to return all possessions that rightfully belong to another.
                    </p>
                    </li>

                    <br>

                    <li id="p3"> <b>THE APOSTOLIC PATTERN</b>
                        <p>
                        The apostles in the Early Church also practised and taught restitution. In fact the whole Church of God lived by this teaching (Matthew 28:19,20; John 14:26; Acts 2:42; 16:4,5; Philemon 7-21). The rules of righteous living are just as high (or even higher) under the dispensation of grace as in the Old Testament. Restitution is as much part of the Gospel as conversion or water baptism. It is not an act of self-righteousness. We should make right wrong matters of money such as theft, dishonest gains unpaid bills, etc. We must also confess lies, slanderous reports, unjust criticisms, hatred, malice, and those words and deeds wherein another has been harmed or hurt, whether he knows it or not. It is better to allow Jesus to plead for us before men here on earth, than that we be confronted with them in eternity (I Timothy 5:24).
                        </p>
                    </li>

                    <br>

                    <li id="p4"> <b>OUR REQUIRED PRACTICE</b>
                        <p>
                        Making restitution can be likened to a spiritual warfare in which we need God‟s help and wisdom to undertake (Ephesians 6:10-12,16). Some restitutions are delicate; these are restitutions that may implicate or bring injuries or harm to others that may be unsaved, or that may require us to settle issues with individuals or organizations that may affect others in an adverse way. They also include cases in which we do not have clear leading from God‟s Word as to what steps to take. For such cases, we need fervent prayers, wisdom and counselling from Spirit-filled preachers of God‟s Word who believe and teach the whole counsel of God (Proverbs 11:14; 15:22; 24:6). <br>
                        <p>God demands that restitution be made. A far reaching benefit in God‟s plan as regarding restitution is that an undeniable testimony is directed to people who otherwise might never seriously hear the story of the Gospel. A religion that compels a man to pay just debts, restore stolen articles and uncover crimes, demands confidence from the people of the world. When restitutions are made, and past wrongs are settled, the peace of God floods the believer‟s heart. There is a confidence toward God in prayer, and the believer makes progress in the Christian life as the sense of guilt is removed (Romans 8:1,2).</p>
                        </p>
                    </li>
              
                    
