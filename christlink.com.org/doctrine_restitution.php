                <p>In our last study we saw the need for <b>genuine repentance.</b> Repentance is the gateway to true saving faith, which is the key to our salvation. One practical side of true repentance is restitution. It is the act of making right our wrongs to fellow men. Repentance is towards God, while restitution is towards men. When a man confesses his sins, he receives forgiveness from God (Proverbs 28:13; I John 1:9). Yet there is a moral responsibility we have towards our neighbour, for many of the sins we commit actually affect other people adversely. In such cases God requires that amendment be made to the injured or wronged person as an evidence of genuine repentance. This promotes a right relationship with fellowmen. We need to bear such fruit to signify to all that we have become new creatures indeed (Luke 3:8-14; Romans 8:1,2; Acts 24:16; II Corinthians 5:17).</p>
