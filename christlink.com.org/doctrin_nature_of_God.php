              <li> <b>THE INCOMMUNICABLE ATTRIBUTES OF GOD</b>
                <p>
                On the one hand, there are attributes of God which are peculiar to Him. These are not shared by man: they are divine. They are called incommunicable, absolute, or immanent attributes. One, God is a Spirit (John 4:24; Acts 17:24; II Corinthians 3:12). That means He is not flesh and blood - He does not have a corruptible (terrestrial, earthly, mortal, perishable) body like man. Yet He is a Person, for He has an incorruptible (celestial, heavenly, immortal, imperishable) body (I Corinthians 15:40). He was neither made nor born. He is not subject to the limitations and temptations of men (James 1:13). Two, God is eternal: He has neither beginning nor end of days (Psalm 90:2; Isaiah 57:15; Deuteronomy 32:40; 33:27; Psalm 135:13; 145:13; I Timothy 1:17; Revelation 1:8). Three, God is infinite (Jeremiah 23:24; Acts 17:27,28; Psalm 139:7-13). He is not subject to the limitation of space: He is omnipresent. There is nowhere man can hide that God cannot fish him out. And four, God is immutable. God is constant - He does not change. Kingdoms rise and fall, but God remains the same. He neither increases nor decreases (Hebrews 1:11,12; James 1:17; Num. 23:19; Mal. 3:6; Psalm 33:11; Proverbs 19:21; Isaiah 14:24; 46:9,10). All these attributes are not shared with man.
                </p>
                </li>
              
                  <br>
                
                  <li> <b>THE COMMUNICABLE ATTRIBUTES OF GOD</b>
                    <p>
                    On the other hand, there are many other attributes of God, which are communicable to man; that is, these attributes are given in a measure to the (regenerate) man. They are called communicable, relative, or transitive attributes. One, God is powerful. In fact He is omnipotent - He can do all things (Genesis 18:13,14; Jeremiah 32:17,27; Matthew 19:26; Luke 1:34-37). But God also endows man with power even to do miracles (Matthew 17:20; Mark 9:23; John 14:12). Two, God has knowledge. He is in fact omniscient - He knows all things (Acts 15:18; John 21:17; Hebrews 4:13). But He has endowed man also with ability to know Him and even have the gift of the word of knowledge - to be able to see, though in a limited scope, through the eyes of the Holy Spirit (II Peter 3:18; I Corinthians 12:8). Three, God is holy. He is glorious in holiness: as to the measure of abundance of His holiness, there is none as holy as the Lord (Exodus 15:11; I Samuel 2:2; Isaiah 6:1-4; Psalm 22:3). But He has also endowed man with His holiness. In fact He commands everyone of His children to be holy. It is wondrous that this distinguishing attribute of God can be shared by mortal men! (Matthew 5:48; Romans 1:7; I Thessalonians 5:27; I Peter 1:15,16). Four, God is just. There is neither partiality nor injustice with God. He is perfect in judgment and uncompromising in standard (Genesis 18:25; Deuteronomy 32:4; Acts 10:34,35; Rom 2:11). He expects the same quality from His own children (Leviticus 19:15; Deuteronomy 16:19; I Tim 5:21; James 3:17). Five, God is good. He rains His blessing on man, He is patient with the sinner, and He is kind to the saint. These all He does without looking for reward or recompense (Romans 2:4; John 3:16). He expects us also to be good to men irrespective of their attitude towards us (Galatians 5:22; Matthew 5:43-48). Six, God is truthful. There is neither lying nor duplicity with God. He is transparently honest (Num. 23:19; Tit. 1:2). This also is a communicable attribute, for God even commands us to abstain from lying, and rather speak the truth from our hearts (Ephesians 4:25; Colossians 3:9). And seven, God is wise. In fact He is the Only Wise God (Dan 2:20; Rom 11:33; I Timothy 1:17). And in His mercy, He shares the same with men. We can receive the gift of the word of wisdom for ministry. And when we even lack wisdom in particular situations, we are encouraged to pray for it, and the Lord shall answer, for every good gift comes from Him (I Corinthians 12:8; James 1:5,17). <br>
                    Knowing these great attributes of God, and having access to so many of them, what then should our attitude be? One, we must worship or reverence Him. Two, we should develop faith in Him, which will move us to obedience. And three, we should give ourselves to prayer and supplication, knowing that in whatever area we may lack, God is more than sufficient for all our need (I Peter 2:17; Revelation 22:9; Genesis 15:1-6; Exodus 34:5-9).
