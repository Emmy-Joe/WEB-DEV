                    <b>Commandment keeping</b> (Rom. 3:28; Galatians 2:16; 3:21; James 2:10,11). God‟s commandments are good and they are for keeping. However, the mere keeping of the commandments has no power to save. The law is our schoolmaster to point us to Christ. The law shows us our transgressions, condemns us as guilty and worthy of punishment but directs to Christ who only can save from the power and penalty of sin.
                  </li>
                  <li>
                    <b>Moral living</b> (Psalm 51:5; Job 14:4; Isaiah 64:6; Romans 3:10-23; John 1:45-50; Acts 10:1-44). The moral living of today will not compensate for the sins of yesterday as well as the inherited nature of sin. All self-righteousness and morality are like filthy rags when examined under God's microscope. Moral living has never and will never save anybody.
                  </li>
                  <li>
                    <b>Religious observances</b> (I Peter 3:21; Romans 6:3-7; Luke 5:30-33; I Corinthians 1:11-17; John 3:1-8; 4:19-26; Acts 10:1-10; 16:16-20). Many try to rigidly keep some religious codes of conduct with the hope that God will be pleased. Confirmation, water baptism, eating the Lord's Supper, doing the work of the “kingdom”, Sabbath keeping, refraining from certain kinds of food, regular praying and fasting, praying five times a day, going on a pilgrimage to holy land, going to places of worship barefooted, bathing with “holy water”, church membership, etc. These are all dead works of religion. They can never save any man from sin.
                  </li>
                  <li>
                    <b>Good works</b> (I Corinthians 13:3; Matthew 16:26; Mark 8:37; I John 5:2; Luke 16:15). Good works are only good when God declares them to be so. What is good in the sight of man may be very evil in the sight of God (Luke 16:15). Many so-called “good” works are done to silence the conscience, to soothe the pain of guilt, to bury our wrongdoing and to justify oneself before God. God is not fooled by such “good” works.
                  </li>
                  <li>
                    <b>Heredity</b> (Ezekiel 18:4; John 8:39-44; Leviticus 5:1-17). Being born into a Christian home, having Christian parents and good religious upbringing does not make a person acceptable to God. God has no grand children. Every man will bear his iniquity. You are personally accountable to God.
                  </li>
                  </ol>
