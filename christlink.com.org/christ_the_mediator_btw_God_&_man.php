                      <li>He asked for the removal of his sins from Go's sight and divine washing. That points to the need for expiation. Sin has a polluting influence on the sinner. Expiation is the divine act of covering sin and cleansing the sinner's heart for a renewed walk with God.</li>
                      <li>He made a specific confession – “this evil.” Sin must be viewed as evil. The attitude in genuine confession is that of sincerity. God desires the truth in the inward part. He requires a broken spirit and a contrite heart. Confession of sin must be honest and specific.</li>
                      <li>He prayed for purging with hyssop, typifying the need for blood. Confession of sin is mandatory. But David knew that confession alone could not save him; he needed cleansing by blood (Exodus 12:22; Leviticus 14:4-6, 49-53; Num. 19:1-9; 16-21; Heb. 9:18-20).</li>
