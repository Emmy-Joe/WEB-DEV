              <li id="p1"> <b>THE REALITY OF HELL</b>
              <p class="mb-4"> <b>Luke 16:19-24.</b> 
                <p>
                Hell is a fearsome “rest-place” for transgressors. And the Lord Jesus Christ spent considerable time lending weight to this reality. He told the city of Capernaum for instance, that it would be turned into hell because of its iniquities (Matthew 11:23). He warned repeatedly on the danger of hell and the need for His listeners to steer clear of it (Matthew 5:22,29,30; 10:28; 18:8,9; Mark 9:43-48; Luke 12:5). He offered vivid explanations of hell as a place of everlasting punishment (Matthew 25:41,46; Mark 9:43-48; Luke 16:22-28; Revelation 14:10,11). He revealed that sinners who died unsaved would have their lot in hell (Matthew 7:15-19; 13:40-42,50; 23:33; 25:41-46; Luke 16:23; Revelation 19:20; 20:14-15; 21:8). <br>
                The apostles also followed the same thematic explanations of hell as offered by the Lord (Romans 2:5-9; II Thessalonians 1:7-9; Hebrews 10:26,27; James 3:5,6; II Peter 2:4,9; 3:7; Jude 7,23; Matthew 3:7-10). The Church today cannot afford to assume a less specific position on the teaching of hell, without subverting the sacredness and completeness of scripture. The word “hell” is found about fifty-four times in the King James version of the Bible. Its translation in Hebrew, Sheol, occurs thirty-one times, while the Greek interpretations Gehena, Hades, and Tartarus occur twelve, ten times and once respectively. Its regular usage in scriptures, apart from the allusions drawn to it by the Lord and other bible characters, lend sufficient validity to its reality.
                </p>
