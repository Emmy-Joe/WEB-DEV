                  <li id="p2"> <b>THE FINALITY OF HELL</b>
                  <p>Luke 16:23-26</p>
                    <p>
                      When a sinners dies, his soul immediately proceeds to hell where, with full consciousness, he tastes the excruciating pain that hell offers. The story of poor Lazarus and the rich man, best exemplifies the eternal punishment awaiting the sinner (Luke 16:23,24). Hell is also a place where the memory of the damned comes alive. The rich man in Luke 16:23,24 remembered his riotous living while on earth as well as his five brothers yet unsaved. Apart from the acute discomfort of hell, the regret of an error of judgment while on earth on the part of the unsaved, presents an everlasting torment on its own. <br>
                      <p>The teaching of the doctrine of hell must scrupulously avoid erroneous and unscriptural views of false teachers. The concept of purgatory, representing a so-called intermediate state between death and appearance before God, has no biblical foundation. Neither is the notion of annihilation of the wicked correct. The future punishment of the wicked is eternal. The sinner receives an express translation, at his death, into hell, “where their worm dieth not, and the fire is not quenched” (Mark 9:44-48; Deuteronomy 32:22; Job 26:6; Isaiah 5:14; Amos 9:2; II Peter 2:4; II Thessalonians 1:7-9). The needless controversy over the reality of hell is not strange; the scriptures clearly allude to this: “But the natural man receiveth not the things of the Spirit of God: for they are foolishness to him; neither can he know them, because they are spiritually discerned” (I Corinthians 2:14). Since God is holy, sin must be punished in the person of the sinner or in the person of the sinner‟s Substitute - the Lord Jesus Christ.</p>
                      No man believes in hell because he simply wants to. He believes in hell because he is compelled to. It is true that eternal punishment is not a pleasant subject for discussion, yet it is a fearful reality, an awful and tragic fate.
                    </p>
                    </li>

