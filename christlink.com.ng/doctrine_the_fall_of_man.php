            <h2 class="text-primary">Introduction</h2>
            <p class="mb-4"> <b>Genesis 2:7-25; 3:1-24</b> <br>
                <i>The subject of sin is of great importance in every religion. In fact, most religious rites are direct consequences of sin. The Christian faith came because of sin. The Bible is the book of sin. The Gospel is the message of sin. Christian ministry is predominantly because of sin. Hell is a consequence of sin. Sin is the transgression of God‟s law. It is the sum total of all unrighteousness. Deliverance from sin is the great object of God‟s redemptive plan. It is therefore necessary for every believer to understand the subject of sin and God‟s provision for its remedy</i> <br>
                We can only appreciate the subject of redemption if we understand the impact sin has made in the life of man. A right understanding of the fall of man will give us insight into God‟s redemption plan and programme. We will then be able to rightly place the sacrifice of Jesus Christ. Moreover, every sinner will then have a basis to exercise intelligent faith in the Person of our Lord Jesus Christ for his personal salvation.
            </p>
  
            <h2 class="text-primary">SUB TOPICS:</h2>
            <p class="mb-4"> </p>

              
            <ol>
              <li id="p1"> <b>THE ORIGINAL STATE OF MAN  [Genesis 2:7-25]</b>
                <p>
                    From the account in our text, the following facts characterised man in his original state:
                    <ul class="list-unstyled ul-check">
                        <li>He possessed divine life – “a living soul” (Gen. 2:7).</li>
                        <li>He was innocent – he had no knowledge or feeling of evil or sin; he knew no shame.</li>
                        <li>He was simple and satisfied. He lived in a garden (Gen. 2:8-10). There is no better garden than that which God planted. Eden means “delight” or “pleasure.” There is no better life than that which is in the centre of God‟s will. The best place to which man can attain is the very place of God‟s appointment for him.</li>
                        <li>He had divine favour (Gen. 2:8-10). God distinguished Adam and Eve. He planted a garden for them and gave them the best accommodation He designed.</li>
                        <li>He was industrious (Gen. 2:15). Even in the state of innocence, man was not made to be idle. God provided Adam a fitting occupation.</li>
                        <li>He had dominion. God gave Adam dominion over all that He had made.</li>
                        <li>There was law and order (Gen. 2:16,17). God constrained man‟s behaviour and choices by a commandment. God commanded Adam and Eve because of His creative right on them. The Maker should rule over the made. And His commandments are not grievous. It is the glory of man to be governed by God – not his will appetite, lust, flesh, ego, satan, etc.</li>
                        <li>God provided a fitting companion for Adam (Gen. 2:2:18,21-25). The state of innocence was not a state of aloneness.</li>
                        <p>This was the pleasant setting which man enjoyed. On top of this, he had unbroken fellowship with God. The presence of God was real. He could see God because he still retained the glory.</p>
                    </ul>
                </p>
                </li>
              
                  <br>
                
                  <li id="p2"> <b>THE TRANSGRESSION AND THE FALL [Genesis 3:1-13]</b>
                    <p>
                    Man did not enjoy the blessed state for long: he fell. For proper understanding, we shall look step by step at the process that led to this fall:
                        <ul class="list-unstyled ul-check">
                            <li>There was a commandment, an object of transgression, and a tempter (I John 3:4).</li>
                            <li>Though in the state of innocence, man was still liable to temptation, just like the saint is, and like Jesus was (Matthew 4:2,3; I Chronicles 21:1; II Corinthians 2:11; I Thessalonians 3:5).</li>
                            <li>The tempter was the devil. His role has not changed since then. He uses whatever is available. In this case, he used the serpent. As for the vocal ability of the serpent, there should be no doubt of its possibility. Moreover, Eve did not find it strange to talk to the serpent, any more than Balaam found it strange to talk to an ass. The devil is still the chief tempter of mankind today. He is the “old serpent” (Revelation 12:9; 20:2; II Corinthians 11:3; I Peter 5:8). The serpent was certainly subtle, yet it took the devil to make it speak so rationally and convincingly. Satan still disguises himself today.</li>
                            <li>The nature of the temptation was first to present God‟s commandment as grievous, and to awaken the woman‟s feelings to the same. Then, he attacked the Word or commandment of God. He instilled distrust in her mind. He lied to the woman. He preached another gospel to her. He appealed to her lust, pride, and curiosity (II Corinthians 11:13-15; Galatians 1:6-8; Isaiah 8:20; I John 2:15-17).</li>
                            <li>The underlying problem of the woman was her lust (Gen. 3:6; 13:10-13; 25:29-33; Jos. 7:21; I Kings 11:1-4; James 1:12,13). The woman saw, in the first place, that the tree was good for food. She was not hungry, for God had provided sufficient for their feeding. She only lusted after “something new,” just like many people desire “another gospel” today (Galatians 1:6). Secondly, she saw that the fruit was pleasant to the eyes. There were many other beautiful trees in the garden. Man always finds especially the forbidden fruit pleasant. That is the reason for adultery – the forbidden woman, fornication – the forbidden experience, theft – the forbidden property, etc. And thirdly, she saw that the fruit was desired to make one wise. They already had sufficient wisdom to know God and keep His commandments. Yet, she lusted after “hidden” wisdom, just like people seek after curious things and experiences today outside the plain, simple, saving gospel.</li>
                            <li>The woman voluntarily took the fruit and ate. She introduced the sin to her husband. She did not fall alone. Sin is contagious (James 1:13-15; Proverbs 30:33).</li>
                            <li>There was an immediate, dramatic effect of their action (Job 31:33; Proverbs 28:13). Their eyes were opened “to know,” but they became ashamed of what they saw. They tried to cover themselves. They felt the pangs of guilt and moved away from the presence of God. Fear came; transparency disappeared. They began to make excuses. Innocence was lost; the glory had departed. Even before judgement came, they knew within themselves that something had gone wrong. They had sinned and come short of the glory of God.</li>
                            <p>Those who argue against the Genesis story of the Fall should compare their experience with this, if there is no striking similarity. Every sinner runs away from God and from the reach of His Truth.</p>

                        </ul>
                    </p>
                    </li>

                    <br>

                    <li id="p3"> <b>CONSEQUENCES OF THE FALL [Genesis 3:14-24]</b>
                        <p>
                        Before judgement was pronounced on man, the rule of conscience had already taken effect. Sin is a spiritual transaction. The effects are always immediate in the spiritual realm. The following consequences followed the fall of man:
                            <ul class="list-unstyled ul-check">
                                <li>The curse came. The serpent was cursed, the woman was cursed, the man also was cursed, and the ground was cursed. Sin never profits anyone, no matter what the excuses may be.</li>
                                <li>Shame came. Clothing became mandatory. Innocence was lost. There was no freedom before God anymore. Sin is the greatest barrier between man and God.</li>
                                <li>Man was ejected from God‟s place of appointment and excluded from the pleasure of His presence. Fellowship with God was lost. Paradise was lost too. Every sinner is a loser.</li>
                                <li>Sin entered the world. Mankind was corrupted. The fountain was already fouled, and consequently, the flow became unclean (Gen. 6:5,11,12; Romans 3:23; 5:12-14; Job. 14:4; 15:14-16). Sin ruins.</li>
                                <li>The devil became the ruler and god of this world. Man‟s dominion was taken away. Satan took control of man, for of whom a man is overcome, of him is he brought into bondage (II Peter 2:19).</li>
                                <li>Mortality entered the world (Romans 5:21). “The soul that sinneth, it shall die.”</li>
                                <li>The judgement of eternal damnation took effect on man. Hell became the destiny of every sinful soul (Ezekiel 18:4; Psalm 9:17). The pleasures of sin will never compensate the pains thereof.</li>
                                <p>The effect of the fall is seen everywhere today. Sin has become universal, and it is man‟s greatest problem. Its consequences are evident in every phase of human existence. The fundamental root of every problem faced by man is sin. Sin ranges from juvenile delinquency to adult irresponsibility, from fornication to adultery, from political oppression to criminal murder, from small-scale pilfering to large-scale robbery, from individual cheating to national exploitation. These things are deeply rooted in man‟s sinful nature. Tougher laws, more prisons, public education, social improvement and all the various methods devised by man to combat crime may bring some relief but they can never and will never bring permanent, workable solution. Man ignores the great problem of sin to his peril. Apart from the sin we inherited from Adam, we commit actual sins of our own – stealing, fornication, adultery, idolatry, effeminacy, homosexuality, witchcraft, drunkenness, violence, murders, etc. (1 Corinthians 6:9-11; Galatians 5:19-21; James 4:1; Matthew 15:19,20). We have to answer for every one of these sins. The wages of sin is eternal punishment in hell fire and separation from God.</p>
                            </ul>
                        </p>
                    </li>
              
                    
