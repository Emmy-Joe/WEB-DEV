                  <li id="p2"> <b>DURING WATER BAPTISM</b>
                  <p>Acts 8:38,39</p>
                    <p>
                    Matt. 3:16; Mark 1:5,10; John 3:23; Matt. 18:19,20 <br>
                      In the first place, it is essential that the he who will baptise another be born again. It is unfortunate when the “minister” himself is a sinner (Matthew 15:14; 23:16; Luke 6:39). Secondly Water Baptism is by immersion, not by sprinkling or pouring water or making a cross on the forehead by water. The meaning of the word itself is very clear. “Baptism” is not an original English word; in fact the Greek word, baptiso, was not translated at all, it was only anglicised. To baptise is the same as to “dip”, “soak”, “drench”, “immerse”, etc. That is why “much water” is necessary, and it was possible for the candidates to come “out of the water”. Thirdly, it is only one immersion, not three! Jesus died only once. And lastly, it must be done “in the name of the Father, and of the Son, and of the Holy Ghost” (Matthew 28:19). Some people teach that it must be done in the name of Jesus only. But this is not scriptural. We must follow the format specified by the Lord Himself, which the apostles did not change (I Corinthians 15:3; Galatians 1:6-9).
                        
                    </p>
