              <li id="p1"> <b>BEFORE WATER BAPTISM</b>
              <p class="mb-4"> Acts 8:35-37</p>
                <p>
                Matt. 3:5-8; Luke 3:10-14; Acts 2:41; 19:4,5,18,19; 16:30-33 <br>
                Genuine repentance of sins, and the exercise of the saving faith in Christ, are prerequisite to water baptism (Acts 20:20,21). Repentance has evident fruits which include confession of sin, the amendment of our past evil deeds, and a sincere determination not to go back into them any more (Proverbs 28:13; Ezekiel 33:15,16). As we believe then on the name of Christ, our sins are forgiven, and our names are written in the Book of Life (I John 1:9; Luke 10:20). It is only then that we are eligible for Water Baptism. Definitely, a baby who is not conscious of right or wrong cannot possibly repent of sin and maintain any saving faith towards Christ; hence infant Baptism is essentially unscriptural. Furthermore, it is not Water Baptism that saves us, as some people erroneously teach. Without a good conscience, an observance of this ordinance avails nothing (I Peter 3:21).
                </p>
