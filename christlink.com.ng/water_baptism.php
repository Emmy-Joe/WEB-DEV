              <p>Water Baptism is one of the two ordinances in the New Testament. The second is the Lord‟s Supper. Water Baptism is compulsory for every believer in the Lord Jesus Christ (Matthew 28:19; Mark 16:16). It symbolises, to the believer, identification with, and partaking of Christ‟s death, burial, and resurrection (Romans 6:3,4). When we submit to water baptism, it is an acknowledgment of Christ in a public way. Therefore it is a ceremony that witnesses to all that one has put on Christ (Galatians 3:27). It is the commandment of Christ which everyone who loves Him must keep (John 14:15). Our obedience to the Gospel is not complete without Water Baptism.</p>
