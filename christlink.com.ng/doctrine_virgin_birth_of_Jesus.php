                    <li id="p3"> <b>THE UNIQUENESS OF THE VIRGIN BIRTH Isaiah 7:14.</b>
                        <p>
                        The virgin birth of Jesus was a unique sign from God. The prophet Isaiah was very clear and definite about it (Isaiah 7:14). It distinguished Him from any other man, and marked Him as the only true Messiah. God bears witness to His approved ministers in various ways. Some of the signs that were recorded concerning Christ and His ministry were also recorded concerning a few other messengers of God. The virgin birth, however, is probably the most unique and unprecedented sign. A voice from the cloud witnessed to the ministry of Christ. But that was not as unique as the virgin birth, for Moses‟ ministry was also attested to by a voice (Num. 12:5-8; Matthew 17:5). An angel announced the conception of Christ; but we also know the births of Samson and John the Baptist were equally announced by angels (Judges 13:2-5; Luke 1:11-15,26-33). Moses, Samson and John the Baptist, were all God‟s messengers, but they all had earthly fathers. They were all descendants of Adam‟s fallen race. The virgin birth is therefore a unique sign from the Lord, conferred on Christ, and He alone. No other living soul has ever been, or will ever be conceived of a virgin. <br>
                        The challenge is for us to come near in reverence and worship. It is in our believing the Word of God in its totality that we shall be established (Isaiah 7:9).
