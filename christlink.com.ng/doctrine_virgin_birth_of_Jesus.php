                  <li id="p2"> <b>PROOFS OF THE VIRGIN BIRTH Matthew 1:20</b>
                    <p>
                    Luke in his gospel narrative recorded that the angel Gabriel was sent to a virgin, betrothed to a man, and the virgin‟s name was Mary (Luke 1:26,27). After the announcement, Mary herself plainly said, “How shall these things be, seeing I know not a man?” (Luke 1:34). Matthew in his gospel, narrated that Mary was espoused to Joseph, and that before they came together, she was found with a child of the Holy Ghost (Matthew 1:18). He further recorded that Joseph knew her not till she had
                    <br> brought forth her firstborn Son, and he called his name Jesus. The Bible says that Joseph was a just man (Matthew 1:19). This means he was not a transgressor of God‟s law. If Mary as an espoused virgin had conceived through a strange man, it would then be right for Joseph to put her away (Deuteronomy 22:23,24; Matthew 19:9). But the angel of God who appeared to Joseph in a dream prevented him from putting her away, for what was conceived of her was of the Holy Ghost (Matthew 1:20). On the other hand, if Joseph had known Mary before Christ was conceived, or was responsible for the conception, it would be unlawful for him to even attempt to put her away (Mal. 2:14-16). The scriptures would not have qualified him to be a just man if he was transgressing God‟s commandment. <br>

                    </p>
                    </li>

