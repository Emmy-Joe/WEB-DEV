                        The divine-human relationship also confirms the God in three Persons perfecting the relationship of man with the Triune God (Ephesians 2:18). The salvation of man is a joint work of the Holy Trinity (John 3:16; 6:44; 3:6-8). Water Baptism involves the Trinity together (Mat 28:19,20). Our prayer also involves the Trinity. We direct our prayers to the Father (Luke 11 :2; 22:42; 23:34,46; John 17:1,5,24-26; James 1:5), through the Son (John 14:13,14; 15:16). And it is the Holy Spirit Who makes our prayers potent and effectual (Romans 8:26). Sanctification is by the Trinity (John 17:14-17; I Thessalonians 5:23; Acts 26:18; Hebrews 13:12; Romans 15:16). The Baptism in the Spirit has to do with the three Persons (Luke 24:49; John 14:15-18; Acts 2:38,39;15:8). Even ministerial gifts are administered to the believer at three levels corresponding to the three Persons in the Holy Trinity (I Corinthians 12:4-6). It is only the perfect work of the Godhead that keeps the believer in grace from day to day (II Corinthians 13:14).<br>
                        Having then the Godhead as our support, we need not fear any evil, for underneath us are the everlasting arms. We should rather be bold as a lion, while we abide in the righteousness of the Lord, and quit ourselves like men (Deuteronomy 33:27; Ephesians 6:10).
