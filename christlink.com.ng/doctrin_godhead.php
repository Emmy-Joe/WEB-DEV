              <li> <b>THE MYSTERY OF THE GODHEAD</b>
                <p>
                The Godhead is a great mystery which is clearly beyond the finite mind of the unsaved, natural man (I Corinthians 2:14). The believer accepts the gospel truth of the Trinity by faith, recognising that God remains the eternal repository of all mysteries (Deut 29:29). He believes that with respect to His being or essence, God is one, and with respect to His personality, God is three. The essence must not be divided or the persons confused. In spite of the great mystery surrounding it, the doctrine of the Godhead has always proved to be eminently rich in spiritual and practical values. And to those willing, the Lord Jesus promises the advent of a blissful reign of the Godhead (John 14:23; Revelation 3:20). <br>
                The Godhead consists of the Father, the Son and the Holy Ghost. The Bible clearly teaches that each member of the Godhead is God – separate, distinct and recognisable personalities perfectly united in one. The Father is called God (I Corinthians 8:6; Psalm 89:26; I Chronicles 29: 10; Mat 6:9). The Son Jesus Christ is also called God (Isaiah 9:6,7; John 20:28), and the Holy Ghost is called God as well (Acts 5:3,4; I Corinthians 3:16,17; 6:19,20; Hebrews 3:7-9; 10:15,16; Jeremiah 31:31-34). From the foregoing passages it is clear the Father, the Son, and the Holy Ghost are three Persons – co-eternal, co- existent and co-equal in power and divine attributes, that make each God. The word “GOD” in Genesis 1: 1 means Elohim, which is the plural Hebrew word for The Strong One -El. Elohim appears more than 2, 700 times in the Old Testament, and this plurality is seen in “Let us make man in OUR image, after OUR likeness.” “The LORD said…let US go down, and there confound their language” – Genesis 1:26; 3:22; 11:6,7; John 14:23; Psalm 2:3. The Godhead is also referred to as The Holy Trinity (Romans 1:20).
                </p>
                </li>
              
                  <br>
                
                  <li> <b>THE UNITY OF THE GODHEAD</b>
                    <p>
                    The unity of the Godhead is scripturally evidenced in the execution of all divine plans and purposes. At creation, a significant fact about the Godhead emerged with the usage of the plural noun, “us” (Genesis 1:26). Elementarily, this implies the involvement of more than one Person in the creation of man. The same word continues to recur, first after the fall of man (Genesis 3:22), and just before He confounded man‟s language (Genesis 11:7). The testimony of scriptures on the ministry of the Lord Jesus Christ reveals the perfect unity of the Godhead. It was God‟s anointing and power of the Holy Ghost that enabled Him to fulfil His ministry (Acts 10:38). <br>
                    The complementary functions of the Trinity are referred to in almost all of the New Testament (Matthew 3:16,17; 28:19; Mark 1:10; John 16:7-15; 6:37,44; Romans 8:16; I Thessalonians 5:23; Ephesians 5:25; I Peter 1:2; I Corinthians 12:4-7; Acts 10:38). The three personalities in the Godhead acted together at the incarnation of the Lord Jesus. God sent His only begotten Son, and anointed Him with the Holy Ghost (Luke 1:26,35). At the baptism of Jesus, the Holy Spirit came bodily upon Him, while the Father made a public proclamation from heaven that Jesus was His Son (Matthew 3:16,17). At the onset of His ministry, Jesus affirmed the companionship of the Father and the Holy Ghost. It was by their combined working that He could fulfil His preaching ministry, healing the sick and delivering the captives (Luke 4: 18).
                    </p>
                    </li>

