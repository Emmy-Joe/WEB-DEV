              <li id="p1"> <b>THE PERSON OF THE HOLY SPIRIT</b>
              <p class="mb-4"> </p>
                <p>
                The Holy Spirit is not a mere impersonal force. He is not a mere power that we can get hold of and use. He is not a mere influence that we feel. He is a Person Who seeks to get hold of us and use us. The Holy Spirit is a Divine Personality Whom we must honour, worship, fellowship with and listen to. He is called the Holy Ghost, the Holy Spirit, the Spirit of the Living God, the Spirit if Christ, the Spirit of Holiness, the Spirit of Life, the Spirit of wisdom, the Spirit of your Father, the Comforter, the Eternal Spirit (Matthew 28:19; II Corinthians 13:14; Luke 11:13; Hebrews 9:14; I Corinthians 3:16; II Corinthians 3:3; I Peter 1:11; 4:14; Romans 1:4; 8:2; Matthew 10:20). The Holy Ghost is eternal, omnipresent, omnipotent, omniscient and holy. He is Inspirer of the Holy Scriptures, the general Overseer of the militant Church, the great Teacher, Comforter, Helper and Guide of all saints. He is the character Builder of all members of Christ‟s Body (Hebrews 9:14; Psalm 139:7-10; Acts 10:38; Matthew 12:28; Luke 11:20-22; I Corinthians 2:10,11; John 14:17-26; II Corinthians 3:18; Galatians 5:22,23). He is the third Person of the holy Trinity (I Corinthians 8:6; Ephesians 4:3-6; Matthew 28:19).
               </p>
