                  <li id="p2"> <b>THE WORK OF THE HOLY SPIRIT</b>
                    <p>
                    There are many things the Holy Spirit does in, for, and through the believer, the Church, and in the world. One, He searches all hearts (John 16:8-11; I Corinthians 2:10,11). Two, He convicts of sin (John 16:7-11). Three, He regenerates (Titus 3:5). Four, He bears witness with us (Romans 8:14-16). Five, He adopts into God‟s family (Romans 8:15). Six, He helps and gives victory in temptation (Romans 8:2,26,27). Seven, He aids us in prayer (Romans 8:26,27; Zech. 12:10). Eight, He fills believers with joy, peace and boldness (Acts 13:49-52). Nine, He sanctifies (Romans 1:4; 5:5). Ten, He quickens and heals our body (Romans 8:11). Eleven, He comforts, counsels and commands (Isaiah 11:1,2). Twelve, He intercedes for the saints (Romans 8:26,27). Thirteen, He illuminates and inspires (II Peter 1:21). Fourteen, He searches and reveals the deep things of God (I Corinthians 2:9-12). Fifteen, He teaches (John 14:26). Sixteen, He guides into all truth (John 16:13). Seventeen, He creates (Psalm 104:30). Eighteen, He empowers (Acts 1:8). Nineteen, He directs the Gospel work (Acts 8:26-29; 16:6-10). And twenty, He confirms the Gospel (Hebrews 2:3,4).
                    </p>
                    </li>

                    <li id="p3"> <b>BIBLICAL BASIS FOR HOLY GHOST BAPTISM</b>
                        <p>
                        The baptism in the Holy Ghost is the endowment of power from on High upon the sanctified believer. It is the promise of the Father and when one receives this gift, it is accompanied by the initial evidence of speaking a language unlearned previously, referred to as “speaking in tongues as the Spirit gives utterance” (Matthew 3:11; Acts 1:8; John 1:8; Luke 3:16; John 1:30-33; 7:37). This language is not humanly, but divinely taught (Acts 2:4). Jesus Christ the great Teacher preached it and promised it (John 7:37-39; Luke 24:49 and Acts 1:4-8).
                        <p>The Baptism with the Holy Ghost is not the new birth, salvation, conversion or regeneration (John 7:37-39; 14:17; 17:6-9,14-16 Luke 10:19-24; 24:49). It is not the sanctification experience (John 17:9; 17:21-23, and Acts 2:1-4). Jesus prayed for the sanctification of His disciples and yet commanded them to wait for the enduement of power - the Baptism with the Holy Spirit. Moreover, sanctification is for purity whereas the Baptism with the Holy Spirit is for power. It is also not illumination, joy, feeling, liberty and victory in prayer, self-denial or zeal (Luke 24:27,31-32,44-45; Acts 8:8,14-17; Matthew 19:27; 16:17).</p>
                        </p>
                    </li>

                    <li id="p4"> <b>RECEPTION OF THE HOLY SPIRIT</b>
                        <p>
                        The mighty outpouring of the Holy Spirit is promised to all flesh, and those who meet God‟s condition will receive God‟s abundance (Isaiah 44:3; Ezekiel 36:25-27; Joel 2:28,29; John 7:37-39). Very few of Old Testament saints had the bulk of the Spirit upon them for some special tasks the Lord gave them to do. But there were prophecies of a better time when the Holy Spirit would be poured on God‟s people in an overflowing measure (Isaiah 44:3; Joel 2:28,29). Inasmuch as prophecies concerning Christ‟s incarnation, birth, life, dominion, rejection, suffering, meekness, death and resurrection have been fulfilled, so also must all prophecies concerning the outpouring of the Holy Spirit be fulfilled (Num. 23:19). There are steps we need to take to be baptized in the Holy Ghost. One, we must strongly desire or thirst for the experience (Isaiah 44:3; John 7:37,39). Two, we must first receive “a new heart” or be sanctified (Ezekiel 36:26,27). Three, there must be faith to receive (Galatians 3:2,14; Mark 11:22,24). And four, we must pray sincerely and earnestly for the experience (Luke 11:13).
