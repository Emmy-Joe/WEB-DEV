            <p class="mb-4"><b>Isaiah 9:6,7</b><br>
              <b>God’s Word teaches that Christ’s Millennial Reign is the 1000 years literal reign of Jesus on earth, which will be ushered in by the coming of Jesus back to earth with ten thousands of His saints. At this time He will judge the nations that dwell upon the face of the earth (Jude 14,15; II Thessalonians 1:7-10). During this time the devil will be bound (Revelation 20:2,3). It will be a reign of peace and blessing (Isaiah 11:6-9; Hosea 2:18; Zech. 14:9-20; Isaiah 2:2-4).</b>
              <p>The word millennium is a compound derivative from Latin which simply translates into a thousand years. Thus, Christ‟s millennial reign is a thousand years of the full manifestation of the glory of God. It is sequel to the Great Tribulation which is a period of intense suffering for all on earth</p>
              <p>According to God‟s revealed timetable, the Church will suddenly be taken away by Christ (I Thessalonians 4:13-18) in a mysterious event known as the Rapture. Then will follow seven years of great suffering of sinners left behind (Matthew 24:21-22). At the end of this period, Jesus will come again with the Church to establish His government (Jude 14). The government of this world, at that time, will resist His coming. But the Lord will overcome at the battle of Armageddon (Revelation 19:15-21). Satan will be bound and imprisoned for 1000 years (Revelation 20:1-3). The kingdom of this world will then become the kingdom of our God and His Christ (Revelation 11:15; Daniel 2:44).</p>
