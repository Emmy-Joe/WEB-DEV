                  <li id="p2"> <b>CHARACTERISTICS OF THE MILLENNIUM</b>
                    <p>
                    What are the components of the millennium? <b>One</b>, the millennium shall witness an unprecedented era of national and individual peace. There will be a complete cessation of the scourge of wars, as the kingdoms of the world will be fused together under the reign of Christ. The ensuing peace will herald an era of economic boom and prosperity. The beauty of this age is that hitherto antagonistic and irreconcilable foes will henceforth co-habit as “sheep and lambs.” <br><b>Two</b>, there will be joy unspeakable (Isaiah 9:3,4; Jeremiah 30:18,19). <br><b>Three</b>, there will be holiness (Ezekiel 43:7-12; Isaiah 1:26,27). <br><b>Four</b>, there will be glory (Isaiah 24:23; 60:1-9). <br><b>Five</b>, there will be comfort (Isaiah 12:1-2; Jeremiah 3:23-25). <b>Six</b>, justice will abound (Isaiah 9:7; 11:5; 42:1-4). <b>Seven</b>, there will be full knowledge (Isaiah 11:1-2,9; 41:19-20; 54:13; Habakkuk. 2:14). Additionally, the following benefits will also accrue to the inhabitants of the age. <b>One</b>, the original curse placed upon mankind at creation will be removed, resulting in abundant productivity to the earth (Isaiah 11:6-9; 35:9; 65:25). <b>Two</b>, sickness becomes a thing of the past (Isaiah 33:24; Jeremiah 30:17; Ezekiel 34:16). And <b>three</b>, healing will naturally be available to the deformed (Isaiah 29:17-19). Against the background of the above, there shall be preservation of life (Isaiah 41:8-14; 62:8,9; Jeremiah 32:27; Ezekiel 34:27; Joel 3:16-17; Amos 9:15; Zech. 14:10,11), and complete freedom from all forms of oppression that at present ravage mankind (Isaiah 14:3-6; 42:6,7; 49:8,9; Zech. 9:11,12).
                    </p>
                    </li>

