              <p class="mb-4"> </p>
                <p>
                Christ‟s millennial reign will be a time when the purposes of God will be fully realised on earth, as a theocracy (government of the state under the firm control and direction of God) will be established. Christ, as the divine Representative of the Godhead will at this time, speak and act for God. He will be given a universal and glorious dominion, with absolute power to govern (Isaiah 9:6; Psalm 45:4; Isaiah 11:4; Psalm 72:4). It will be the responsibility of Christ at this age to announce God‟s will and law (Isaiah 2:3,4; 33:21,22; 42:4; Acts 3:22). The millennial reign will be a manifestation of the promise God gave David, that his throne would have no end. Christ, as David‟s Son after the flesh, will thus establish a glorious house and throne, over an equally glorious kingdom (II Samuel 7:12-16; Psalm 72; Isaiah 11:10). Divine mercy, divine goodness and divine truth will be displayed through Christ even as the glory associated with His deity, omniscience, omnipotence and righteousness will be fully made manifest.
                <p>The Gentile world system (humanity without Christ) as it is at present organised under the direct influence of satan, will thus become an outmoded setup. In its stead comes Christ‟s millennial reign void of sin and all forms of unrighteousness. Satan, having been defeated and bound, will lack the power to induce people to sin (Revelation 20:1-6; Matthew 19:28; Ephesians 5:25-27).</p>
               </p>
