                  <li id="p2"><b>DIVINE HEALING AND OUR REDEMPTION</b>
                    <p>
                      Redemption is a release or liberation from captivity or death by paying a price called a ransom. It simply connotes buying back unto freedom someone in servitude, slavery or captivity. The fall of man brought all human race under sin, Satan and condemnation as a result of man‟s disobedience to God. Man lost his purity and favour with God in the fall, and consequently, all men became sinners and captives to sin, Satan and sickness (Romans 5:12). God then undertook the redemption of man by sending His only begotten Son to die in man‟s place. He being capable of redeeming man met the demands of the law and became the legal substitute by paying the redemptive price for those who are condemned to death because of breaking the law. This is absolute protection from eternal punishment through the blood of the perfect and spotless substitute (Exodus 12:13; John 3:16; Ephesians 1:7; Romans 3:24; Revelation 5:9; Romans 8:3; Isaiah 53:4-8; I Corinthians 6:20; 7:23; Matthew 26:28; I Peter 1:18-21; Genesis 3:16-24). No sinner can be acquitted except he accepts Christ as his Redeemer (Acts 4:12).
                      <p>But man‟s redemption is not limited to removal of guilt; it includes freedom from all consequences of the broken law, which are sickness, disease and satanic oppression. Healing of sickness and disease as well as continued health is thus provided for all men through the sacrificial death of Jesus Christ. Jesus not only bore our sins, He also bore our sickness and diseases. He became our sickness-bearer in exactly the same way He became our sin-bearer. Christ was scourged and striped for our healing; nailed to the cross for our sin (John 3:14; 12:22-23; Isaiah 53:4,5; Matthew 8:16,17; Psalm 103:2,4). The Holy Spirit concluded by saying, “Christ hath redeemed us from the curse of the law”; sickness - pestilence, fever, cancer, ulcer, boils, as well as nameless sicknesses found in people‟s lives today and other infirmities recorded or not recorded in scriptures (Deuteronomy 28:15-29,58-61; Galatians 3:13; Deuteronomy 7:12-15). Christ has been manifest to destroy the works of the devil (I John 3:8; Acts 10:38; Hebrews 13:8). In the Old Testament, there was a divine plan by God healed all those who were in covenant relationship with Him. The promise to keep the Israelites from diseases by which the Egyptians were afflicted was made at the instance of the healing covenant Israel had with God. God is still in the business of healing today where the conditions of the covenant are met.</p>
                      As a child of God, divine healing and health is yours (Matthew 15:26-28; Ephesians 2:12-21). Sickness in whatever form is from the devil. God calls it captivity (Job 42:10), Jesus calls it bondage (Luke 13:12,16) and the Holy Spirit calls it oppression (Acts 10:38). The victory of Jesus is our victory. He did nothing for Himself. He conquered sin, Satan, disease and sickness on our behalf. He shares His victory with all who will repent and believe on His Name. He bore our infirmities, sicknesses and diseases so that we could claim full release and deliverance.
                    </p>
                    </li>

                    <li id="p3"> <b>REQUIREMENTS FOR HEALING AND HEALTH</b>
                        <p>
                        The Bible sets forth some conditions to fulfil in order to enjoy the benefits and blessings of our relationship with God. What does God demand from you? One, be genuinely converted (Psalm 103:2-5; Matthew 13:15). Two, obey God and His Word (Exodus 15:26; 23:25,26; Proverbs 4:20-22). Three, abide in Him (Psalm 91:1-7; John 15:1-7). Four, make peace with fellowmen (Mark 11:22-26; James 5:14-16; Genesis 20:14,17). Five, serve God in holiness (Colossians 2:6,7; Exo 23:25,26). Six, pray the prayer of faith (Matthew 7:7,8; James 5:13; I Peter 2:24; II Peter 1:3,4; Hebrews 13:8). God heals those who are under the healing covenant by His Word (Psalm 107:20), through His mercy (Matthew 20:20-24), by His power (Matthew 28:18; Luke 5:17), by His Spirit (Acts 10:38; Isaiah 61:1,2), in His name (Acts 3:6; Mark 16:17,18), and by His will (John 5:30; I John 5:14,15; III John 2).
                        <p>Believe God‟s promise for your healing, fulfil the conditions, lay hold on God through prayer, and then, according to your faith, it shall be done unto you (I John 3:22).</p>
                        </p>
