              <p class="mb-4"> </p>
                <p>
                How does sickness come? Most often infirmities come as a result of disobeying God (Genesis 20:1-18; I Chronicles 10:13), through satan, the very source of sickness and affliction (Job 2:6-7; Matthew 4:23,24; John 10:10; 8:44; 5:14; Acts 10:38), backsliding (Exodus 10:10; II Chronicles16:12-14; Matthew 12:43-45), misuse of the tongue (Num. 21:4-6; 11:1-3; 12:1-10), covetousness, or using God‟s gift for money (II Kings 5:20-27), rebellion (Psalm 107:17-20; Deuteronomy 32:39-41), or by breaking God‟s law of nature. The Bible reveals that evil spirits assist satan to afflict man. They possess, oppress, torment, obsess and cause dumbness and deafness (Matthew 9:32,33; Mark 9:25-27), blindness (Matthew 12:22), grievous vexation and torments (Matthew 15:22), lunacy (Matthew 4:24; Luke 9:38-42) and suicide (Matthew 17:15). They gain entrance through sin, consulting with familiar spirits, palm reading, magic, witchcraft, fortune telling, not guarding the mind or refusal to resist the devil. As long as you have any property from the devil, he will stay around you. Renounce and destroy the works of the devil so as to be totally free (I Peter 1:1,13; 5:8,9; Acts 19:18,19).
               </p>
