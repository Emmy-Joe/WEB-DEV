                The central theme of the Bible is CHRIST and HIS WORK OF REDEMPTION (John 20:31; Luke 24:25-27; Revelation 1:1). To fully understand the Bible and rightly interpret its message, we must first be converted to Christ and receive Him as Saviour. Those who do not know, or who have wrong conceptions about Christ, cannot appropriately discern the message of the Bible, because the carnal mind cannot understand the things of the Spirit of God (I Corinthians 2:14).
