                <p>
                The Holy Bible is the inspired Word of God, and it is the only source of knowledge of the Almighty God to all mankind. It is the contact point between finite man and the infinite God, and its purpose is to give mankind a revelation of God (II Timothy 3:15-17). The Bible is severally referred to as the Holy Scriptures (Romans 1:2; II Timothy 3:15), the Oracles of God (Romans 3:2; Hebrews 5:12), the Book of the Lord (Isaiah 34:16), the Book of the law (Jos. 1:8), the Word of God (Mark 7:13), the Word of Christ (Colossians 3:16), and the Word of Life (Philippians 2:16). It comprises two parts: the Old and the New Testaments. It has 66 books, 39 of which are in the Old, while 27 are in the New Testament. The Bible was originally written in Hebrew and Greek. The Old Testament was written in Hebrew and Aramaic, and the New in Greek. There were over 40 authors drawn from disparate generations spanning a period of over 1800 years, used by God to write the Bible. And even though their occupation, education and social classes were divergent, their theme was the same as they were inspired by the Holy Ghost (II Peter 1:20). The inspiration of the authors can be attested to by some peculiar characteristics of the Bible, which are not found in any other book written by man. Such characteristics include its wonderful unity despite the fact that the authors lived at different ages, its versatility as it meets the needs of all people irrespective of race and language (Matthew 4:4), its superiority to other books, its influence on individuals and nations, its preservation over the centuries, its fulfilled prophecies (Isaiah 7:14; Ezekiel 36:19; II Chronicles 7:19), and its perfection (Psalm 19:7; James 1:25). <br>
