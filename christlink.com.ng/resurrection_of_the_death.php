              <b>God’s Word teaches the resurrection of the dead as clearly as the immortality of the soul. Every individual who has ever lived will be resurrected, some to honour and glory, and others to shame and everlasting contempt (Job 19:25-27; Psalm 71:20; Isaiah 26:19; Daniel 12:2; John 5:28,29; I Corinthians 15:12-57; I Thessalonians 4:13-16; Hebrews 6:1,2; Philippians 3:8-11; Revelation 20:4,6,12,13).</b>
              <p>The resurrection of the dead is a cardinal and an important doctrine of the Bible. All who die in this world will undergo physical resurrection before the Great White Throne Judgment. This doctrine shows that there will be a resurrection of the body, joined with the soul, to meet the LORD either in peace and joy or meet Him as a Judge in condemnation, eternal punishment and torment of hell fire.</p>
