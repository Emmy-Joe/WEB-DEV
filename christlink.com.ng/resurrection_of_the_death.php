            <ol>
              <li id="p1"> <b>BIBLICAL FOUNDATION FOR THE DOCTRINE</b>
              <p class="mb-4"> </p>
                <p>
                Jesus, the Prophet come from God, declared in John 5:25, “Verily, verily, I say unto you, The hour is coming, and now is, when the dead shall hear the voice of the Son of God: and they that hear shall live.” The fact of the resurrection of the just and unjust runs through the teaching of Jesus Christ (John 6:40; Matthew 16:21; 22:23-32). Job spoke of his eyes and flesh seeing God after worms have destroyed his body (Job 19:25-27). Isaiah spoke of the earth casting out the dead (Isaiah 26:14-19). Daniel spoke of the awakening of many that sleep in the dust, some to everlasting life, and some to shame and everlasting contempt (Daniel 12:2). In like manner spoke David (Psalm 16:10; 17:15). Old and New Testament saints (Hebrews 11:35; Mat 28:1-20; Mark 16:1-18; Luke 24:1-49; John 19-21; Acts 17:18,32; I Peter 1:3; 3:21). Even Herod the wicked king and the generality of the people of his day believed in the resurrection of the dead (Mark 6:14-16).
               </p>
                </li>
              
                  <br>
                
                  <li id="p2"> <b>THE SOUL AND THE RESURRECTION</b>
                    <p>
                    In the intervening period between death (when the body and soul are separated) and the resurrection, one may ask, “Where is the soul?” The soul of a saint of God goes immediately to meet God in heaven. The repentant thief on the cross received forgiveness and assurance from Jesus: “Today shall thou be with me in paradise” (Luke 23:39-43). Righteous Lazarus died and was “carried by angels into Abraham’s bosom”, where he was comforted (Luke 16:19-31). Stephen at death said, “I see the heavens opened, and the Son of Man standing on the right hand of God” ready to receive him (Acts 7:54-60). Paul was “willing rather to be absent from the body and to be present with the Lord” (Philippians 1:21-23). Many other references of scripture confirm that the soul of the saint will rejoice in God‟s presence after death (II Corinthians 5:1-8; Ecclesiastes 12:1,7). But when a sinner dies, his soul proceeds to hell. In hell fire, he is conscious, he can see, he can feel and he can hear (Luke 16:19-31).
                    <p>The form to be associated with the resurrected body of the just is exemplified by the glorified body that Jesus, the first fruit, had after his resurrection (Luke 24:36-43; John 20:11-20,24-31; I John 3:2). All resurrected bodies will be immortal (Daniel 12:2; Mark 9:42-48). The resurrected bodies of saints will possess different degrees of glory. “There is one glory of the sun, and another glory of the moon, and another glory of the stars: for one star differeth from another star in glory. So also is the resurrection of the dead” – (I Corinthians 15:39-54).</p>
                    </p>
                    </li>

                    <br>

                    <li id="p3"><b>THE TIMING OF THE RESURRECTION</b>
                        <p>
                        The timing for the resurrection to life of the just and resurrection to judgment of the unjust is well spelt out in the scriptures in accordance with the ordained programme of God. The resurrection of life (John 5:28-29), resurrection of the just (Luke 14:13-14), a better resurrection (Hebrews 11:35), or the first resurrection (Revelation 20:4-6) has four phases:
                          <ol>
                            <li>The resurrection of Christ, the first fruit (I Corinthians 15:3,4,12,20,23).</li>
                            <li>The resurrection of the Church-age saints at the Rapture (I Thessalonians 4:13-16).</li>
                            <li>The resurrection of the tribulation period saints (Revelation 20:3-5).</li>
                            <li>The resurrection of Old Testament saints at the second advent of Christ to the earth (Daniel 12:2; Isaiah 26:19).</li>
                          </ol>
                          <p>All the saints of God that ever died shall rise in the first resurrection (Revelation 20:6). The second resurrection is still part of God‟s programme, but it deals with the unsaved dead. It is the resurrection to damnation (John 5:29). Partakers of this resurrection shall be punished with eternal torment in the lake of fire (Daniel 12:2; Revelation 20:11-15). There is a difference of one thousand years between the first and second resurrections (Revelation 20:5-15).</p>
                          But just as in the Early Church when Hymenæus and Philetus erred about the resurrection (II Timothy 2:17), there are many false teachings today also, such as annihilation and purgatory. Nevertheless, the doubt or error of man cannot alter God‟s timetable. Whether men repent and get saved or not, just as Jesus‟ resurrection did not depend of the faith of men, the dead shall surely rise (Acts 23:8; II Timothy 2:15-18; I Corinthians 15:12-23; Romans 3:3,4).
                          <p>The challenge is for you as an individual to make sure you are ready for the first resurrection, so that the second death will have no power over you. To partake of the first resurrection, there must first be a spiritual resurrection here on earth. As a sinner dead in sins and trespasses, you must first be made alive by the Spirit of life from above, and brought in the newness and holiness of life (I Thessalonians 4:14,16; II Corinthians 5:17-19; Hebrews 12:14).</p>
                        </p>
                    </li>
                  </ol>
