              <li> <b>DESCRIPTION OF THE RAPTURE</b>
                <p>
                The Rapture is not the same as the Second Coming of Christ. It is rather the first phase of the Second Advent. At the Rapture, Christ will appear in the air, invisible to the inhabitants of the earth. He will come to resurrect all dead saints, who along with the living believers will put on immortality and shall be caught up to be with the Lord. It will happen “in the twinkling of an eye,” before the great and terrible day of the Lord, when the wrath of God will be poured down from His cup of indignation without mixture at the Great Tribulation (I Corinthians 15:52). The Second Advent will not take place until after great tribulation. The time of the Rapture is unknown, even by the angels (Matthew 24:36; II Thessalonians 2:1-5). It will be heralded by the voice of the archangel and the blowing of trumpets signalling the end of the Church age (I Thessalonians 4:13-15). <br>
                The Rapture was a mystery, unknown to the Old Testament Saints and prophets. God had underlined this great event in two different ways in the Scripture to assure us of its certainty. First, it was illustrated in Enoch and Elijah, the two living saints who did not taste death, but were translated in a moment of time (Genesis 5:24; II Kings 2:11,12). Second, it was illustrated in Christ in the New Testament. He died, was buried and resurrected. While he talked with His disciples on Mount Olivet, He was taken up, and a cloud received Him out their sight (Acts 1:9-11). <br>
                The Rapture marks the end of the Church Age, also referred to as the Time of the Gentiles (Luke 21:24). This truth was veiled to the Saints of old. However, “known unto God are all His works from the beginning of the world.” In His mercy, He interrupted His programme for Israel with the Church Age. Christ the Messiah came for His people but they rejected Him (John 1:11), and God turned to the Gentiles to take a people for Himself. The Rapture will mark the end of this special period of grace. God will resume His programme with Israel immediately after the Rapture, thus ushering in the beginning of the seventieth week in Daniel‟s prophecy (Daniel 9:24-27).
