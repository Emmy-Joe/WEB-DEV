            <h2 id="p0" class="text-primary">INTRODUCTION</h2>
            <p class="mb-4"><b>(Proverbs 22:6)</b><br>
            Children are God‟s gift to parents. They occupy a special place in God‟s purpose to populate the world. Children are very precious in God‟s sight, and He is greatly concerned about their welfare (Psalm 127:3-5). Child training is therefore a major responsibility in marriage. It is because God wants children to be raised in a family setting that He has coupled childbirth directly to marriage and not to a baby manufacturing company or cloning. It is the responsibility of parents to train up their children in the way they should go – not the way they want to go, or the way the world wants them to go. Child training is extremely important, because it determines the success and earthly joy of our children, and it can have a serious impact on their eternal destiny. God holds (Christian) parents responsible for the training of their children (I Samuel 3:13).
           </p>
  
            <h2 class="text-primary">SUB TOPICS:</h2>
              
            <ol>
              <li id="p1"> <b>PURPOSE OF TRAINIG</b>
              <p class="mb-4"> Psalm 144:11,12; Luke 2:40,52.</p>
                <p>
                Training goes beyond ensuring the physical welfare of children. It is a conscious and continuous act of exercising and developing the child towards a wholesome character. Training should therefore be aimed at the following:
                  <ul class="list-unstyled ul-check">
                    <li>To give the child biblical instruction, insight and example, to the end that he may know, love and serve the Lord till eternity.</li>
                    <li>To equip the child with the means to develop a noble character, and exercise him therein, so that he may become a dependable and responsible adult. He should not become a social misfit.</li>
                    <li>To enrich him with knowledge, understanding and wisdom to enable him take wise decisions in life among contrasting and conflicting voices and examples.</li>
                    <li>To prepare him for future independence in terms of occupation, finance and family life.</li>
                  </ul>
               </p>
                </li>
              
                  <br>
                
                  <li id="p2"> <b>PITFALLS IN TRAINING</b>
                    <p>I Samuel 3:13; Ephesians 6:1-4.</p>
                    <p>
                    Many modern adults do not train their children at all. The result is a crop of delinquents and social misfits. But for those of us who are aware of the necessity to train our children, it is important to know some pitfalls in training. These include:
                      <ul class="list-unstyled ul-check">
                        <li><b>Ignorance:</b> This is a major problem for many Christian parents. They are ignorant of the child‟s thinking, his needs, his environment, peer pressure, his anxieties, what he learns, and what he can do. This results in lack of communication, distrust, and evasiveness. The child then prefers to ask questions from people outside and learn from them, rather than his parents. He then obtains a disguised and possibly wrong view of many crucial matters of life.</li>
                        <li><b>Indiscipline</b> or indulgence (I Kgs 1:5,6; Pro. 29:15). Some parents hardly correct their child. Right from babyhood, they feed his stubbornness, selfishness and greediness. The result is lack of direction, respect and moral values. He may develop relational problems in adulthood.</li>
                         <li><b>Inconsistency</b> (Mat. 12:25,26). Parents who uphold a rule today and relax it tomorrow, teach the child to disregard rules. Where there is inconsistency in standard, word, and action or between partners, the child takes advantage of the chaos, and manipulates his way through life.</li>
                         <li><b>Inconsideration</b> Selfishness, cruelty, neglect of care, etc, plague many children. What some parents cannot bear, they lay on their children. Rather than caring for their children, some parents lavish money on clothes, expensive cars, etc.</li>
                    </ul>
                    </p>
                    </li>

                    <br>

                    <li id="p3"><b>PATTERN IN TRAINING</b>
                        <p>
                        The Bible provides us timely principles for the successful training of our children. Child training is work. It will not happen automatically, any more than an unmanned ship can arrive at its intended destination. Those who decide to have children must realise and accept their role as parents. Negligence in this brings them under the judgement of God. The following are guidelines for child training:
                          <ul class="list-unstyled ul-check">
                            <li>There should be clear goals for the training (Psalm 144:11,12). This will serve as a reference point along the way. It also allows us to overcome discouragement as we keep the noble vision tenaciously before us. These goals should be coupled with the purpose mentioned above.</li>
                            <li>Parents must set coherent and reasonable standards in the home, and adhere to them (Exodus 18:20; Pro. 1:8,9). It is the parent that determines what is right, and not the child or the environment. Make the child aware of what is right and what is wrong. He needs to know what is acceptable and the bounds of his freedom.</li>
                            <li>It is important to keep an open communication channel with the child. Parents must work at building trust in the child and winning his confidence. This will allow him to speak out his mind, and give room for early correction.</li>
                            <li>Create in the child a sense of self-worth, confidence and faith. It is important to appreciate him, affirm your love to him, and encourage him to attempt great things. Refrain from cutting comments. Motivate him to be his best in everything (Eph. 6:4).</li>
                            <li>Fairness is essential. Whether we deal with one child or many, there should be uniform treatment. Good behaviour should be commended and encouraged, while wrongdoing should be proportionately punished and discouraged. The welfare of the child should be central (Pro. 13:24).</li>
                            <li>Support the child in prayer (Job 1:1-5). Understand the need for God to build the child up, and take time to bring him regularly to the throne of grace. As you adult face spiritual warfare, so does the child. Therefore, he needs your intercession. Pray that the presence of the Lord will overshadow him, and that no evil will overcome him. Teach him to pray also. That will enable him to depend on God in the future.</li>
                            <li>Lastly, complement the training with a convincing and supporting example. What we build in precept can be destroyed by a contradicting example, whereas, what we omit in teaching will be supplemented by a consistent, exemplary character. Actions speak louder than voice. Be trustworthy, faithful and consistent.</li>
                          </ul>
                          <p>Child training is an awesome responsibility. Lack of training makes the child a failure and social misfit. Proper training gives the child a good foundation in life. As we approach this issue with faith and prayer, may the Lord enable us to raise our children for His glory in Jesus‟ name. Amen.</p>
                        </p>
                    </li>
