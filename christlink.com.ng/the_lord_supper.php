              <li id="p1"> <b>THE SCRIPTURAL MEANING OF THE LORD’S SUPPER</b>
              <p class="mb-4"> </p>
                <p>
                The institution of the Lord‟s Supper is clearly shown in the gospels (Matthew 26:17 -30; Luke 22: 1,7-20). Paul amplified the doctrine in I Corinthians 11:20-30. In Luke we see the links between the feast of Passover and Unleavened Bread and the Lord‟s Supper (Luke 22:15-20). In fact the earlier was a type of the latter; the Old Testament type is fulfilled by the New Testament antitype (Exodus 12:1-28,40-51). Just as Jesus‟ sacrificial death on the Cross has replaced the Levitical sacrifice, even so has the Lord‟s Supper replaced the Passover as an ordinance. Some Churches refer to this ordinance as a sacrament (Latin sacramentum), meaning “Holy Ordinance” (I Corinthians 11:2). Theologians call it the “Eucharist” (meaning “giving of thanks”). Still others call it “The Lord‟s Table” or “The Holy Communion” (I Corinthians 10: 16). Those who participate in eating the Lord‟s Supper are called “Communicants”.
               </p>
                </li>
              
                  <br>
                
                  <li id="p2"> <b>THE OLD TESTAMENT SIGNIFICANCE OF THE ORDINANCE</b>
                    <p>
                    We can draw valuable lesson from the study as we look at the parallel between the Lord‟s Supper and the (Old Testament) Passover. Passover means to spare (Exodus 12:13,27). Israel was in bondage in Egypt (Exodus 2:11). They cried unto the Lord for deliverance (Exodus 3:7-10). Pharaoh would not set them free until God plagued Egypt heavily (Exodus 3:19,20). The Passover was instituted on the night of the last plague when God killed all the firstborn of Egypt and “passed over” the Jews. The feast of the Unleavened Bread was joined with the Passover, and thereafter both came to be referred to as the Passover (Deuteronomy 16:1-8). Sanctified elders and the priests handled the emblems (the blood of pure Iamb and unleavened bread). The Iamb was to be roasted and not eaten raw. No bone thereof must be broken (Exodus 12:46; John 1:29; 19:36). This typified Christ. The defiled, the uncircumcised strangers and the Gentiles did not participate in this feast (Exodus 12:43-49). A qualified Jew who refused to eat of the Passover, would be cut off from Israel (Numbers 9: 13). Those who by ceremonial uncleanness missed the Passover were given a second chance in the second month (Numbers 9:9-11; II Chronicles 30:1-27). There was thorough preparation before the Passover. There was personal cleansing, corporate cleansing, provision of the emblems and preparation of the place of the ordinance. History tells us that any trace of leaven (impurity) was removed from the location of the Passover. The HALLEL, i.e. Psalms 113-118, was sung (Matthew 26:30), and the story of Exodus 12:1-14 recounted, bringing into remembrance the deliverance from Egypt. Prayers were also offered. It was a holy convocation, which brought joy and renewal of strength to God‟s people (II Chronicles 30:25,26; 31:1-19).
                    </p>
                    </li>

                    <br>

                    <li id="p3"> <b>THE NEW TESTAMENT PRACTICE OF HOLY COMMUNION</b>
                        <p>
                        By the time Jesus came, this holy ordinance, as other Old Testament ceremonies, had been commercialised (John 2:13-17). Jesus instituted the Lord‟s Supper or Holy Communion, signifying the end of the Old Testament era (Luke 22:1-20). Jesus is our Paschal Lamb or Passover (I Corinthians 5:7). He has delivered us from bondage, sin, sickness and satan. There are a number of things we need to note in this New Testament practice. One, communicants are believers, not the unsaved. Two, even these believers should make sure they are living right. There should be personal examination. Malice, unforgiveness, etc. must be settled before going to the Lord ‟s Table (I Corinthians 11:27-32; Matthew 5:23,24). Three, the Lord‟s Supper is not the common love feast or wedding reception food (I Corinthians 11:20-22). Four, we must discern the Lord‟s Body (I Corinthians 11:29). This implies we recognise that the emblems REPRESENT or SYMBOLISE the Lord‟s broken body and His blood that was shed. They are only symbolic, not material. This calls for solemnity. Five, the purpose is to “shew forth the Lord‟s death till He come.” As often as we take it, we call to remembrance His love and passion on the Cross, and we look forward to the Rapture and the Marriage Supper of the Lamb (Matthew 26:17-30; Luke 22:7-13; I Corinthians 11:26; Revelation 19:7-9). Lastly, every believer must ensure he is in a right state of life to take the Holy Communion. On the one hand, partaking thereof “unworthily” attracts divine judgement (I Corinthians 11:30). And on the other hand, if you always feel disqualified to partake of it, you are not likely to be qualified for the Rapture and the Marriage Supper of the Lamb (Revelation 19:7-18).
                        </p>
