                <p>
                Sanctification is an instantaneous experience given to a believer to enable him cleave to God without the tendency to want to go astray or away from the Lord. You do not grow INTO sanctification, but you can grow IN sanctification. Again, sanctification is not brought about by death. To assume that is to say that death is no longer our last enemy to be destroyed (I Corinthians 15:26). Neither are we cleansed from the original stain (the nature we brought into the world) through Water Baptism. It is also not of works, but it is the work of God, divinely wrought by the Holy Sprit. “Holiness”, “Perfection” and “Sanctification” are the definitive terms used interchangeably. Holiness means entire freedom from sin; wholeness, spiritual health, moral integrity and purity. As our bodily health means the absence of sickness, so also does holiness mean the absence of sin (Luke 1:74,75; Psalm 29:2; 93:5; 1 Pet. 1:16; Hebrews 12:14). “Perfection” means that which is not lacking in that which it ought to have. The perfection which God requires and accomplishes in those who earnestly ask in faith is the purifying of our hearts; loving every believer as Christ loves us and loving our neighbours as ourselves. Nothing less than this is desirable and nothing more is required (Colossians 1:28). Entire sanctification is described as “clean heart” or “pure heart” (Psalm 24:3-4; Matthew 5:8). The Bible teaches that all of God‟s children can be like Jesus in separation from the world, in purity and in love. A blameless life is a life in which God sees nothing to condemn. We may not be blameless to our contemporaries, but we can be blameless before God. When God examines a sanctified heart, He finds all that he possesses to be in harmony with Himself. His heart is a throne on which God reigns without a rival; an empire wherein He exercises unchecked, undisputed dominion and authority.
               </p>
