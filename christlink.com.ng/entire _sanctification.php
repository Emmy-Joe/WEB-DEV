            <p class="mb-4"><b>Hebrews 12:14</b> <br>
              <b>God’s Word teaches that Entire Sanctification is a definite act of God’s grace, subsequent to the New Birth, by which the believer’s heart is purified and made holy. It cannot be attained progressively by works, struggle or suppression, but it is obtained by faith in the sanctifying blood of Jesus Christ. Holiness of life and purity of heart are central to Christian living (Luke 1:74,75; John 17:15-17; 1 Thessalonians 4:3,7,8; 5:22-24; Ephesians 5:25-27; Hebrews 2:11; 10:10,14; 13:11,12; Titus 2:11-14 ; 1 John 1:7; Hebrews 12:14; 1 Peter 1:14-16).</b>
                <p>The doctrine of entire sanctification is central in the mind of the Godhead. The doctrine is at the hub or nucleus of Christian teaching. What then is the meaning of the word, Sanctification? Sanctification is an English word which means: to make sacred or holy; to set apart for a holy or a religious use; to make free from sin; to cleanse from moral corruption and pollution. It is the act of God‟s grace by which the affections of men are purified or separated from sin and the world and exalted to supreme love to God. It is to purify in order to prepare for divine service and for partaking of holy things; to free from the power of sin; to be set free from the power of cancelled sin. It is a gracious work of the Holy Spirit, not of works, not of growth, not of death, not of purgatory. God has the ability to destroy sin. Sanctification is an operation of the Spirit of God on those who are already in Christ. They who are effectually called and regenerated having a new heart and a new spirit created in them need to be further sanctified through the virtue of Christ‟s death and resurrection, His Word and His Spirit.</p>
           </p>
  
