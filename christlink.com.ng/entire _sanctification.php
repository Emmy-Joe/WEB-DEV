                  <br>
                
                  <li id="p2"><b>CONDITIONS FOR SANCTIFICATION</b>
                    <p>
                    How does the earnest seeker get sanctified? On the one hand, there must be a definite separation and withdrawal from all sins and appearance of evil (I Thessalonians 5:22; II Corinthians 6:17); from immorality and all unclean things (II Timothy 2:21). And on the other hand, there must be ENTIRE Consecration. This is giving ourselves unto God in a vivid manner; first, we give ALL we are to God. Here we no longer lay any claim on ourselves (Luke 9:60; Romans 12:1-2). Second, we give our body unto the glory of God, not for any selfish motive or purpose in view, but with the consideration and knowledge that we are bought by the precious blood of Jesus. Third, we give OURSELVES FOR SERVICE (John 13:12-17). We should not stress our being priests and kings unto God but as being servants. Here Jesus shows how to consecrate and dedicate ourselves for service. Washing the feet was the work of a slave. So, we must learn to do the meanest job in the midst of believers and not waiting for compliments. Fourth, we must die to self. “Verily, verily I say unto you, except a corn of wheat fall into ground and die, it abideth alone; but if it die, it bringeth forth much fruit” (John 12:24-26). Consecration is coming before God with our past, present, future, personality, possession, wisdom, talent, intellect, money, influence, position in the family and in the church and dedicating all to God. Having been so separated and dedicated unto God, God will then be ready to do His part and give us a definite experience which is Entire Sanctification. This is the operation of God, a definite act of God‟s grace in believers‟ hearts (Hebrews 2:11; 13:12,13).
                    </p>
                    </li>

                    <br>

                    <li id="p3"><b>ENTRANCE INTO SANCTIFICATION</b>
                        <p>
                        God has provided His Word, His Blood, His Faith, His Spirit and Himself as free gift for sanctification (John 15:3; Hebrews 13:12,13; Acts 26:18; I Thessalonians 2:23). It is God‟s perfect will that we be sanctified (I Thessalonians 4:3-8; I John 5:14,15). This holiness does something in our hearts that will make us live right. We must desire it and pray fervently in faith for it (Mark 11:24). The experience can be kept by abiding in Christ, watching and praying (John 15:4; Mark 13:32-35). We should not grieve the Holy Spirit, relapse into bitterness or harshness of spirit, unprofitable conversation, unkind criticism, evil speaking, faultfinding, self indulgence, prejudice,impatience, indolence, negligence, self management or showing interest in objects of temptation (Ephesians 4:30,31; 5:3,4).
