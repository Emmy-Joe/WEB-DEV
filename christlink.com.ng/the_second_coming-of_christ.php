              <li id="p1"> <b>CERTAINTY OF CHRIST’S COMING</b>
              <p class="mb-4"> </p>
                <p>
                The Holy Spirit through the apostles confirmed that Christ shall appear “the second time” (Hebrews 9:28), and would then reward His faithful servants with crowns of glory, praise and honour and His adversaries with fiery indignation (I Peter 5:4; 1:7; Hebrews 10:27). James the apostle also encouraged believers: “Be ye also patient, stablish your hearts: for the coming of the Lord draweth near.” The angel of God confirmed that “this same Jesus which is taken up from you into heaven, shall so come in like manner as ye have seen him go into heaven” (Acts 1:11).
                <p>The prophecy in Isaiah 9:6 that the government (of the whole world) would be on Christ‟s shoulder, is yet to be fulfilled. Also some prophecies in Isaiah 61 concerning Christ were only partially fulfilled at His first coming. Christ Himself confirmed in Luke 4:21 that “this day is this scripture fulfilled in your ears.” The rest of the prophecies in that chapter would be fulfilled after His second advent.</p>
                The exactitude with which the old time prophecies concerning the first coming of Christ were fulfilled points to the infallibility of the Scriptures and to the certainty of those prophecies concerning His second advent. Prophecies on His virgin birth, details of His earthly life, His death and resurrection were all fulfilled to the letter, and so would those relating to His second coming. Apart from the fulfilment of the prophecies concerning Christ, other divine purposes which the second advent would fulfil include (i) the comfort of the church (John 14:1-3; 16:20-22); (ii) the restoration of Israel to her sovereignty as prophesied in the scripture (Isaiah 32:18; 33:20-24); (iii) the judgment of the earth (Revelation 20:11-15; John 5:22); (iv) the renovation of the earth (Isaiah 65:17; 66:22 Revelation 21:1-5); (v) the restoration of all things according to the desire of God (Romans 8:20-23; Jeremiah 12:4,11).
               </p>
                </li>
              
                  <br>
                
                  <li id="p2"><b>SIGNS OF HIS COMING</b>
                    <p>
                    The scriptures abound with evidence that speaks of the certainty of Christ‟s second coming. Our Lord Jesus spoke of His return several times, once leading the disciples to enquire about the signs that would herald that certain event. A study of the scriptures indicates that certain prophecies concerning Christ, and some future programme of God are not yet fulfilled and would only be, after the second advent of Christ. However, all the signs preceding the Second Advent are being fulfilled daily. Going through the Olivet Discourse of Matthew 24 and 25, the following signs of His coming are clear. Jesus Himself said, there would be deception, famines, pestilences, earthquakes, anti-Semitism (Mark 13:9,13), offences, betrayals, hatred, false prophets, lawlessness and martyrdom (Matthew 24:4-13,24). He said the impact of the second coming would be as universal as when “the lightning cometh out of the east and shineth unto the west.” It would make “all the tribes of the earth to mourn.” They would be deep in spiritual lethargy and indifference as was the case in the time of Noah‟s flood (Matthew 24:27,30,37).
                    </p>
                    </li>

                    <br>

                    <li id="p3"> <b>VICTORY AT HIS COMING</b>
                        <p>
                        The greatest event that will precede the second coming of Christ is the Rapture, which lies in the valley between the mountains of Christ‟s first and second coming. It is a part of the mystery of the church which in the Old Testament age “was not made known unto the sons of men” as it was revealed in the New Testament to “His holy apostles and prophets by the Spirit.” The Rapture is however different from the second coming. The two events are distinct and separated by seven years. The second coming refers to Christ‟s coming to the earth to live as He did in the first coming, to be seen by every eye. In the Rapture, Christ would not come to the earth but stop in the air where the saints will meet him. So only believers shall see Him then. When Christ meets the saints in the air, He will take them to heaven where they would remain for the period of the Great Tribulation. It is during this seven-year period that the marriage supper of the Lamb (Revelation 19:18,19), and the judgment of saints for reward shall take place in heaven (I Corinthians 3:12-15; II Corinthians 5:10). In the second coming, however, He will come to the earth to live and fulfil a definite part of God‟s timetable for the earth and man. Christ shall then come with the saints raptured seven years earlier. Whereas the Rapture can take place any moment without warning, the second coming cannot happen until after both the Rapture and the Great Tribulation.
                        <p>In His Second Coming, Christ will reign on the earth for one thousand years with the saints. Christened the Millennial Reign, Christ‟s rule shall be characterized by unprecedented peace, prosperity and blessing on the earth (Isaiah 11:1-9). Satan, the author of heartache, strife and contention would for the duration of Christ‟s Millennial Reign (Revelation 20:23). Satan and his cohorts will mobilise forces to fight Christ and to frustrate His coming again to establish the Millennial Reign, just in the same way he used Herod to stop Christ‟s survival at His first coming. He was defeated and again he will be defeated. The battle against Christ and His host is called the Battle of Armageddon (Revelation 19:11-21; 20:1-4). There will be a great and terrible slaughter of the armies of the Antichrist gathered from the nations of the earth through the efforts of the evil spirits sent out over the earth by Satan, the Antichrist and the false prophet (Revelation 17:13,14). This Satan‟s last attempt to wrongfully take the Kingdom from Christ will fail.</p>
                        The world and the church are called to prepare for this great event. The world is admonished to repent and come to Christ before the day of the Lord (John 3:1-8). The Church as a bride is called to be ready, holy, spotless, watchful, prayerful and busy preaching His word until He comes (Hebrews 12:14; I John 3:1-3; II Timothy 4:1,2; Ephesians 5:25-27).
